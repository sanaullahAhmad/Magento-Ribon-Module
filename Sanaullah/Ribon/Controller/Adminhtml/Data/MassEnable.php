namespace Sanaullah\Ribon\Controller\Adminhtml\Data;

use Sanaullah\Ribon\Model\Data;

class MassEnable extends MassAction
{
    /**
     * @param Data $data
     * @return $this
     */
    protected function massAction(Data $data)
    {
        $data->setLink(true);
        $this->dataRepository->save($data);
        return $this;
    }
}
